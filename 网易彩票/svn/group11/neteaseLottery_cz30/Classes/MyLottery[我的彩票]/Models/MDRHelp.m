#import "MDRHelp.h"

@implementation MDRHelp

- (instancetype)initWithDict:(NSDictionary *)dict {
    if (self = [super init]) {
        
        self.ID = dict[@"id"];
        self.title = dict[@"title"];
        self.html = dict[@"html"];
        
    }
    return self;
}
+ (instancetype)helpWithDict:(NSDictionary *)dict {
    return [[self alloc] initWithDict:dict];
}


@end
